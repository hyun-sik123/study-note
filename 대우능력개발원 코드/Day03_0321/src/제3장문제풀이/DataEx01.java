public class DataEx01 {
	public static void main(String[] args) {

		char a='a'; //A
		char b="a";//B, 단일문자 타입은 작은따옴표로 해야한다. 또는 char을 String 문자열 타입으로 변경해도 된다.
		String c="a";//C
		String d='a';//D, String타입을 char타입으로 변경하든지 작은따옴표를 큰따옴표로 변경하면 된다.
		char e="ab";//E, char을 String타입으로 변경해야 한다.
		String f="ab";//F
		
		//정답은 B,D,E
	}
}
